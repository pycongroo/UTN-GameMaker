import java.awt.Color;

public class Bando {
	private String nombreBando;
	private Color colorBando;
	
	public String getNombreBando() {
		return nombreBando;
	}
	public void setNombreBando(String nombreBando) {
		this.nombreBando = nombreBando;
	}
	public Color getColorBando() {
		return colorBando;
	}
	public void setColorBando(Color colorBando) {
		this.colorBando = colorBando;
	}
	
	
}
